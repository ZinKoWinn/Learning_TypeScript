var x = 5;
var y = 3;

console.log('Value of x is : ' + x);
console.log('Value of y is : ' + y);
console.log('Bitwise AND : & : x and y : ' + (x & y));
console.log('Bitwise OR : | : x or y : ' + (x | y));
console.log('Bitwise XOR : ^ : x xor y : ' + (x ^ y));
console.log('Bitwise Left Shift : << : x left shift y : ' + (x << y));
console.log('Bitwise Right Shift : >> : x right shift y : ' + (x >> y));
console.log('Bitwise Right Shift With Zero : >>> : x right shift with zero y : ' + (x >>> y));